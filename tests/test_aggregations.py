class TestAggregateByModel:
    def test_sum_metrics(self):
        df = _make_test_df()
        result = aggregate(df, ["Модель"])
        assert len(result) == 2
        m1 = result[result["Модель"] == "M1"].iloc[0]
        # Маржа п1 M1: 2000 + 5000 = 7000
        assert m1[col_p("Маржа, ₽", "п1")] == pytest.approx(7000)
        # Продажи шт п1 M1: 80 + 150 = 230
        assert m1[col_p("Продажи, шт", "п1")] == pytest.approx(230)

    def test_margin_rate_from_aggregated(self):
        """Маржа до СПП, % = sum(Маржа) / sum(Продажи) * 100."""
        df = _make_test_df()
        result = aggregate(df, ["Модель"])
        m1 = result[result["Модель"] == "M1"].iloc[0]
        expected_margin_rate = (2000 + 5000) / (8000 + 15000) * 100
        assert m1[col_p(CALC_MARGIN_RATE, "п1")] == pytest.approx(expected_margin_rate, rel=1e-2)

    def test_deltas_present(self):
        df = _make_test_df()
        result = aggregate(df, ["Модель"])
        assert "Маржа, ₽ Δабс" in result.columns
        assert "Маржа, ₽ Δ%" in result.columns


class TestTotalKPI:
    def test_total_margin(self):
        df = _make_test_df()
        kpi = total_kpi(df)
        # Total Маржа п1: 2000 + 5000 + (-500) + 100 = 6600
        assert kpi[col_p("Маржа, ₽", "п1")] == pytest.approx(6600)

    def test_total_margin_rate(self):
        df = _make_test_df()
        kpi = total_kpi(df)
        # Маржа до СПП, % = 6600 / 29000 * 100 = 22.76
        expected = 6600 / 29000 * 100
        assert kpi[col_p(CALC_MARGIN_RATE, "п1")] == pytest.approx(expected, rel=1e-2)


class TestEdgeCases:
    def test_zero_sales_group(self):
        """Если в группе суммарные продажи = 0 → Маржа до СПП, % = NaN."""
        from src.aggregations import _agg_period
        df = pd.DataFrame({
            "Модель": ["M1"],
            col_p("Маржа, ₽", "п1"): [100],
            col_p("Продажи до СПП, ₽", "п1"): [0],
            col_p("Оборот продаж, дни", "п1"): [30],
            col_p("Заказы, шт", "п1"): [10],
            col_p("Заказы до СПП, ₽", "п1"): [1000],
            col_p("Продажи, шт", "п1"): [8],
        })
        result = _agg_period(df, ["Модель"], "п1")
        margin_rate = result[col_p(CALC_MARGIN_RATE, "п1")].iloc[0]
        assert np.isnan(margin_rate)


