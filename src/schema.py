# ---------------------------------------------------------------------------
# Pydantic-модель для валидации загрузки
# ---------------------------------------------------------------------------

class UploadValidation(BaseModel):
    missing_id_cols: List[str] = []
    missing_metric_cols: List[str] = []
    type_conversion_errors: List[str] = []
    critical_warnings: List[str] = []
    info_warnings: List[str] = []

    @property
    def is_critical(self) -> bool:
        return bool(self.missing_id_cols)

    @property
    def has_warnings(self) -> bool:
        return (
            bool(self.critical_warnings)
            or bool(self.info_warnings)
            or bool(self.type_conversion_errors)
            or bool(self.missing_metric_cols)
        )

    @property
    def has_critical_warnings(self) -> bool:
        return bool(self.critical_warnings) or bool(self.type_conversion_errors)

    @property
    def has_info_warnings(self) -> bool:
        return bool(self.info_warnings) or bool(self.missing_metric_cols)
