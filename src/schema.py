def col_p(base: str, period: str) -> str:
    """Имя колонки с суффиксом периода: col_p('Маржа, ₽', 'п1') → 'Маржа, ₽ п1'."""
    return f"{base} {period}"


def all_metric_cols() -> list[str]:
    """Все колонки метрик (п1 + п2)."""
    cols: list[str] = []
    for b in METRIC_BASES:
        cols.append(col_p(b, "п1"))
        cols.append(col_p(b, "п2"))
    return cols


REQUIRED_COLS = ID_COLS + all_metric_cols()

